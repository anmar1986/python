# Integer
print(type(10))
print(type(100))
print(type(-1000))
print(type(-10000))

# Float
print(type(10.1))
print(type(100.2))
print(type(-1000.4))
print(type(-10000.5))

# Complex Numbers

mycomplex = 5+6j
print(type(mycomplex))
print("my reaal part {}".format(mycomplex.real))
print("my imag part {}".format(mycomplex.imag))

